    private void RendelbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RendelbtnActionPerformed
        boolean vanfizetesM = FizetesMCb.getSelectedIndex() > 0;
        boolean vanAlap = PizzaAlapcob.getSelectedIndex() > 0;
        boolean NincsNev = NevTf.getText().equals("");
        boolean NincsTszam = telefonTF.getText().equals("");
        boolean NincsCim = CimTF.getText().equals("");
        int ikon = 0;
        String uzenet = "";
        String cim = "";
        JOptionPane.showMessageDialog(null, "Teszt", "Teszt", JOptionPane.WARNING_MESSAGE);
        if (vanAlap & vanfizetesM & !NincsNev & !NincsTszam & !NincsCim) {
            informaciok();
        } else if (!vanfizetesM) {
            ikon = JOptionPane.WARNING_MESSAGE;
            uzenet = "Nincs kiválasztva a Fizetés Mod!";
            cim = "Nincs Fizetés mod!";
        } else if (!vanAlap) {
            ikon = JOptionPane.WARNING_MESSAGE;
            uzenet = "Nincs kiválasztva alap!";
            cim = "Nincs alap!";
        } else if (NincsNev) {
            ikon = JOptionPane.WARNING_MESSAGE;
            uzenet = "Nincs név megadva!";
            cim = "Nincs Név!";
        } else if (NincsTszam) {
            ikon = JOptionPane.WARNING_MESSAGE;
            uzenet = "Nincs Telefon szám megadva!";
            cim = "Nincs telefon szám!";
        } else if (NincsCim) {
            ikon = JOptionPane.WARNING_MESSAGE;
            uzenet = "Nincs cím megadva!";
            cim = "Nincs cím!";
        }

        JOptionPane.showMessageDialog(null, uzenet, cim, ikon);
    }//GEN-LAST:event_RendelbtnActionPerformed

