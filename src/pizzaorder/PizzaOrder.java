    private int informaciok() {
        String Cim = "Rendelés";
        String RendInf = "Meg Rendelt Pizzárol : ";
        String nev = NevTf.getText();
        String telefon = telefonTF.getText();
        String cim = CimTF.getText();
        String feltet = feltetek();
        String meret = meret();
        int db = (int) DbSpin.getValue();
        String format = String.format("%s\nRendelő neve: %s\nTelefon Szám: %s\nKiszálítási cím: %s\nFeltétek a pizzára: %s\nKiválasztot Méret: %s\nRendelt mennyiség: %d", RendInf, nev, telefon, cim, feltet, meret, db);
        int valasz = JOptionPane.showConfirmDialog(rootPane, format, Cim, JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (valasz == JOptionPane.YES_OPTION) {
            System.exit(0);
        }
        if (valasz == JOptionPane.NO_OPTION) {
            java.awt.EventQueue.invokeLater(new Runnable() {
                public void run() {
                    new PizzaOrder().setVisible(true);
                }
            });
        }
        return valasz;
    }

    private String feltetek() {
        Boolean[] feltetek = {SonkaCkb.isSelected(), PaprikaCkb.isSelected(), Sajtckb.isSelected(), Kukoricackb.isSelected(), Szalamickb.isSelected()};
        String[] feltetekNeve = {"sonka ", "paprika ", "sajt ", "kukorica ", "szalámi"};
        String txt = "";
        for (int i = 0; i < feltetek.length; i++) {
            if (feltetek[i]) {
                txt += feltetekNeve[i];
            }
        }
        return txt;
    }

    private String meret() {
        String txt = "";
        for (Enumeration<AbstractButton> buttons = MeretBtmG.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                return button.getText();
            }
        }

        return txt;

    }

